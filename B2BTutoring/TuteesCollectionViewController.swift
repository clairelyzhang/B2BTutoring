    override func collectionView(collectionView: UICollectionView, cellForItemAtIndexPath indexPath: NSIndexPath) -> UICollectionViewCell {
        let cell = collectionView.dequeueReusableCellWithReuseIdentifier(reuseIdentifier, forIndexPath: indexPath) as! TuteeCollectionViewCell
        
        cell.tuteeName.text = ""
        cell.tuteeProfile.image = nil
        
        // Configure the cell
        User.objectWithoutDataWithObjectId(tutees[indexPath.row].objectId).fetchInBackgroundWithBlock { (object: PFObject?, error: NSError?) -> Void in
            if error == nil {
                if let tutee = object as? User {
                    cell.tuteeName.text = tutee.firstname + " " + tutee.lastname
                    tutee.profileThumbnail?.getDataInBackgroundWithBlock({
                        (imageData: NSData?, error: NSError?) -> Void in
                        if imageData != nil {
                            cell.tuteeProfile.image = UIImage(data: imageData!)
                        } else {
                            print(error)
                        }
                    })
                }
            }
        }
    
        return cell
    }

    func collectionView(collectionView: UICollectionView, layout collectionViewLayout: UICollectionViewLayout, insetForSectionAtIndex section: Int) -> UIEdgeInsets {
